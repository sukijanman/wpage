<div class="rai-copy rai-three">
<p><span id="docs-internal-guid-fcd8c63a-dc8a-5ee1-16cd-2b98d026cf16">Overseen by the George Washington University&nbsp;<a href="https://trustees.gwu.edu/" target="_blank">Board of Trustees</a>, our leadership team is committed to providing a unique and rigorous education to our students while creating a path of transformative growth to further solidify our position as one of the world&rsquo;s premier <a href="/research">research</a> universities. </span></p>

<p><span>Led by chair Nelson A. Carbonell, Jr., a 1985 alumnus of the university, the board&nbsp;is responsible for our university&#39;s governance. The board&nbsp;plays a key role in:</span></p>

<ul>
