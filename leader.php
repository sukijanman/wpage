<h2>Student Leadership</h2>

<p><br />
Student leaders take shape in many forms from members of the <a href="http://gwcse.orgsync.com/org/sa" target="_blank">Student Association</a> to captains of <a href="/athletics-recreation">varsity athletics</a>&nbsp;and <a href="https://studentengagement.gwu.edu/directory-student-orgs" target="_blank">student organization</a> presidents.GW students don&#39;t just participate in campus life; they stimulate it and consistently make their mark.</p>

<p>All student leaders, regardless of their role, work to start a conversation and make progress on an issue or topic. This could&nbsp;mean speaking out against sexual assault as part of a national movement through the <a href="http://itsonus.org/" target="_blank">It&rsquo;s On Us campaign</a> or holding meetings with administrators to make plans for the future.</p>

<p>The current Student Association president is junior Peak Sen Chua.</p>

