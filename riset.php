                        <p>Can the health risks of&nbsp;<a href="https://gwtoday.gwu.edu/heart-disease-and-diabetes-risk-linked-sedentary-lifestyle" target="_blank">prolonged sitting</a>&nbsp;be countered? What happens after a&nbsp;<a href="https://gwtoday.gwu.edu/what-happens-after-death-black-hole"
                            target="_blank">star dies</a>? How do people survive in&nbsp;<a href="https://gwtoday.gwu.edu/how-survive-arctic" target="_blank">the Arctic</a>? Can we better&nbsp;<a href="https://gwtoday.gwu.edu/gw-partnership-measures-poverty-beyond-income"
                            target="_blank">define poverty</a>? Are we&nbsp;<a href="https://smhs.gwu.edu/news/closer-cure" target="_blank">closer to a cure</a>&nbsp;for cancer? Is it possible to&nbsp;<a href="https://gwtoday.gwu.edu/looking-silence-voices-schizophrenia"
                            target="_blank">silence schizophrenia</a>?</p>

                        <p>These are just some of the questions that researchers at the George Washington University are trying to answer, but the possibilities for what they will discover are limitless. Home to traditional disciplines as well as <a href="https://research.gwu.edu/centers-institutes"
                            target="_blank">more than 80 centers and institutes</a>, our research spans science, technology, health, policy, global security, arts and humanities. Our faculty are driving progress in many different fields &mdash; everything
                            from advancing human health and improving current technologies to expanding our understanding of the universe. But no matter what the field, given our influence in the nation&rsquo;s capital, the work conducted here is sure
                            to have an impact far beyond our campus.</p>

                        <p>With support from the&nbsp;<a href="https://research.gwu.edu/" target="_blank">Office of the Vice President for Research</a>&nbsp;(OVPR), cross-disciplinary work is highly encouraged. OVPR facilitates the interdisciplinary research
                            performed by several <a href="https://research.gwu.edu/institutional-initiatives" target="_blank">institutional research initiatives</a>.</p>

                        <p>GW&rsquo;s research enterprise has been growing over the past decade, and in 2015, we opened <a href="https://seh.gwu.edu/" target="_blank">Science and Engineering Hall</a>&nbsp;(SEH), the epitome of GW&rsquo;s investment in infrastructure
                            that enables cutting-edge research and teaching.</p>

                        <p>GW&rsquo;s unparalleled location and state-of-the-art facilities encourage students, faculty and staff to cultivate new collaborations and innovative solutions as they work with fellow researchers and policymakers alike.</p>

                        <p>If you&rsquo;re destined to make a discovery, you&rsquo;ll be right at home at GW. Here opportunities are endless and our partnerships with powerful institutions are longstanding.</p>

